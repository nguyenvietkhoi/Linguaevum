function opttableselect(tablesel) {
    quocngu = tablesel;
    $("#opttablename").html(opttablelist[tablesel]);
    $("#brahpad").focus();
    loadABGD();
}

function loadABGD() {
    var tmpabdg = abugidadata[quocngu];
    var strabgd;
    for (i = 0; i < 14; i++) {
        strabgd += "<tr>";
        for (j = 0; j < 12; j++) {
            strabgd += "<td onclick=\'putWord(\""+abugidadata[quocngu][i * 12 + j].toString()+"\")\'>" + abugidadata[quocngu][i * 12 + j] + "</td>"
        }
        strabgd += "<\tr>";
    }
    $('#abgd').html(strabgd);
}

function putWord(instr) {
    var txtarea = $("#brahpad").val();
    $("#brahpad")[0].selectionStart = $("#brahpad")[0].selectionEnd;
    var caretbeg = $("#brahpad")[0].selectionStart;
    var caretend = $("#brahpad")[0].selectionEnd;
    $("#brahpad").val(txtarea.substring(0, caretbeg) + instr + txtarea.substring(caretend, txtarea.length));
    $("#brahpad")[0].selectionStart = $("#brahpad")[0].selectionEnd = caretbeg + instr.length;
}